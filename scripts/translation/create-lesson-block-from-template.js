#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

function parseArgs(argv) {
  const args = {
    input: 'docs/development/translation/hsk4_lesson_order_template.json',
    output: 'docs/development/translation/hsk4_shang_lesson_01_draft.json',
    start: 0,
    count: 40,
    book: 'shang',
    lesson: 1
  };

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '--input') args.input = argv[i + 1];
    if (token === '--output') args.output = argv[i + 1];
    if (token === '--start') args.start = Number(argv[i + 1]);
    if (token === '--count') args.count = Number(argv[i + 1]);
    if (token === '--book') args.book = String(argv[i + 1]);
    if (token === '--lesson') args.lesson = Number(argv[i + 1]);
  }

  return args;
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function main() {
  const root = process.cwd();
  const args = parseArgs(process.argv.slice(2));

  const inputPath = path.resolve(root, args.input);
  const outputPath = path.resolve(root, args.output);

  const source = readJson(inputPath);
  const entries = Array.isArray(source.entries) ? source.entries : [];

  const block = entries
    .slice(args.start, args.start + args.count)
    .map((entry, idx) => ({
      level: 4,
      book: args.book,
      lesson: args.lesson,
      lessonOrder: idx + 1,
      character: entry.character,
      pinyin: entry.pinyin,
      english: entry.english,
      sourceOrder: entry.sourceOrder
    }));

  const payload = {
    version: 1,
    generatedAt: new Date().toISOString(),
    note: 'Draft block generated by source order. Validate against textbook before apply.',
    entries: block
  };

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');

  console.log('[ORDER] Lesson block draft generated');
  console.log(`- Input: ${inputPath}`);
  console.log(`- Output: ${outputPath}`);
  console.log(`- Book/Lesson: ${args.book} / ${args.lesson}`);
  console.log(`- Source range: ${args.start}..${args.start + Math.max(0, args.count - 1)}`);
  console.log(`- Entries: ${block.length}`);
}

main();
