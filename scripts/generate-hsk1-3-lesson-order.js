function generateEntries(lessonData, level) {
  const entries = [];
  for (const [lessonStr, words] of Object.entries(lessonData)) {
    const lesson = Number(lessonStr);
    words.forEach((word, idx) => {
      entries.push({
        level,
        lesson,
        lessonOrder: idx + 1,
        character: word.character,
        pinyin: word.pinyin,
        english: word.english
      });
    });
  }
  return entries;
}

